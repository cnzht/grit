#turtle.py
import turtle as t
t.setup(800,500,0,0)
t.penup()
t.bk(200)
t.pensize(15)
t.pencolor("red")
t.pendown()
t.circle(180)
t.seth(0)
t.penup()
t.fd(460)
t.pendown()
t.circle(180)
t.seth(90)
t.penup()
t.fd(180)
t.seth(180)
t.fd(180)
t.pendown()
t.fd(100)
t.penup()
t.fd(360)
t.seth(270)
t.pendown()
t.fd(360)
t.seth(360)
t.fd(820)
t.seth(90)
t.fd(360)
t.done()
